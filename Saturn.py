class Saturn(Planet.Planet):
	def Name(self):
		return "Saturn"

	def calculateInfo(self,date):
		if date != self.lastUsedDate:
			self.lastUsedDate = date

			self.calcPosition(date)
			self.calcLightDelay(date)
			self.illuminatedFraction()
			self.getRingInfo(date)
			self.calcMag()

	def getRingInfo(self,date):
		T = date.J2000c()
		i = Degree.new(28.075216 - 0.012998*T + 0.000004*T*T)
		Omega = Degree.new(169.508470 + 1.394681*T + 0.000412*T*T)
		B = asin(sin(i)*cos(self.Betadelay)*sin(self.Lambdadelay-Omega) - cos(i)*sin(self.Betadelay))
		a = (375.35/60)/self.earthDist
		b = a*sin(B.abs())
		N = Degree.new(113.6655 + 0.8771*T)
		lp = self.Ldelay - Degree.new(0.01759)/self.Rdelay
		bp = Degree.new(0.000764)*(cos(self.Ldelay-N)/self.Rdelay)*-1 + b

		U1 = atan2(sin(i)*sin(bp) + cos(i)*cos(bp)*sin(lp-Omega),cos(bp)*cos(lp-Omega))
		U2 = atan2(sin(i)*sin(self.Betadelay)+cos(i)*cos(self.Betadelay)*sin(self.Lambdadelay-Omega),cos(self.Betadelay)*cos(self.Lambdadelay-Omega))
		dU = U1.abs()+U2.abs()

		self.B = B
		self.dU = dU

	def calcMag(self):
		self.M = -8.88 + 5*log10(self.R*self.earthDist) + 0.044*self.dU.abs().deg() - 2.60*sin(self.B.abs()) + 1.25*pow(sin(self.B),2)

	def loadTerms(self):
		self.Lterms = Lterms
		self.Bterms = Bterms
		self.Rterms = Rterms

		self.B = None
		self.dU = None
	def imageName(self):
		return "Saturn_symbol.png"


